defmodule Operation do

  @moduledoc """
  This module implements basic mathematical operations
  which are used by the Calculator module.
  """

  def add(number1, number2) do
    number1 + number2
  end

  def substract(number1, number2) do
    number1 - number2
  end

  def multiply(number1, number2) do
    number1 * number2
  end

  def divide(number1, number2) do
    number1 / number2
  end
end
